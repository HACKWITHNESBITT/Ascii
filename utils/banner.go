import "strings"

// GenerateLogo generates banner logo from text
func GenerateLogo(text string) string {
	lines := make([]string, 8)
	for _, char := range text {
		patterns, ok := font8x8[char]
		if !ok {
			patterns = font8x8['?']
		}
		for i := 0; i < 8; i++ {
			lines[i] += patterns[i] + "  "
		}
	}
	for i := range lines {
		lines[i] = lines[i][:len(lines[i])-2]
	}
	return strings.Join(lines, "\n")
}
